
/* Estrutura de repetição do while com teste lógico no final */

// Primeiro executa o bloco e depois faz o teste, se o teste for verdadeiro faz o looping e executa o bloco novamente, quando for falso o fluxo termina e sai da estrutura.

var c = 1
do {
    console.log(`Passo ${c}`)
    c++
} while (c <= 6)


